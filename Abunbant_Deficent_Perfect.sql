SET SERVEROUTPUT ON;
SET AUTOCOMMIT ON;

CREATE TABLE ABUNDANT(
	num NUMBER
);

CREATE TABLE DEFICIENT(
	num NUMBER
);

CREATE TABLE PERFECT(
	num NUMBER
);

DECLARE
	N NUMBER;
	i NUMBER;
	j NUMBER;
	temp NUMBER;

BEGIN
	N:=&N;

	FOR i IN 1..N LOOP
		temp:=0;
		FOR j IN 1..(i-1) LOOP
			IF MOD(i,j)=0 THEN
				temp:=temp+j;
			END IF;
		END LOOP;
		IF i<temp THEN
			INSERT INTO ABUNDANT VALUES(i);
		ELSIF i>temp THEN
			INSERT INTO DEFICIENT VALUES(i);
		ELSIF i=temp THEN
			INSERT INTO PERFECT VALUES(i);
		END IF;
	END LOOP;
END;
/
SELECT * FROM ABUNDANT;
SELECT * FROM DEFICIENT;
SELECT * FROM PERFECT;
 

DROP TABLE ABUNDANT;
DROP TABLE DEFICIENT;
DROP TABLE PERFECT;	
